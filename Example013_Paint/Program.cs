void PrintArray(int[,] pic)
{
    for(int i = 0; i < pic.GetLength(0); i++)
    {
        for(int j = 0; j < pic.GetLength(1); j++)
        if(pic[i, j] == 0) Console.Write($" ");
        else Console.Write($"+");
    Console.WriteLine();
    }
}

void Paint(int row, int col)
{
    if(picture[row, col] == 0)
    {
        picture[row, col] = 1;
        Paint(row -1, col);
        Paint(row, col -1);
        Paint(row +1, col);
        Paint(row, col +1);
    }
}

